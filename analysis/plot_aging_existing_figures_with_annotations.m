fieldUsage = inspectFieldUsage(state.pauseRuns);
defs = resolveDefinitionReferences(repoRoot);
writeReport(reportOut, plotSources, fieldUsage, defs, dataLog, combineInfo, ...
    figOutFig, figOutPng);

fprintf('Saved: %s\n', figOutFig);
fprintf('Saved: %s\n', figOutPng);
fprintf('Saved: %s\n', reportOut);

