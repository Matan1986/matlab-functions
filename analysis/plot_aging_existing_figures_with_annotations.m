combineInfo = struct();
combineInfo.n_axes_delta = numel(deltaAxes);
combineInfo.n_axes_decomp = numel(decompAxes);
combineInfo.n_axes_total = numel(allAxes);
end

function addCanonicalAnnotation(hFig)
txt = sprintf([ ...
    'Definitions:\\n' ...
    'DeltaM = M_pause - M_noPause  (canonical definition)\\n' ...
    'FM = baseL - baseR            (signed background / step)\\n' ...
    'dip = DeltaM - DeltaM_smooth  (residual / AFM component)\\n\\n' ...
    'Notes:\\n' ...
    '- This figure is generated using EXISTING pipeline plotting functions\\n' ...
    '- This is a PRE-CANONICAL visualization artifact\\n' ...
    '- Some quantities may still depend on legacy/non-canonical extraction paths\\n' ...
    '- Metric-based quantities (e.g. window dip, tau_dip) are NOT physical observables']);

annotation(hFig, 'textbox', [0.50 0.01 0.49 0.20], ...
    'String', txt, ...
    'Interpreter', 'none', ...
    'EdgeColor', [0.3 0.3 0.3], ...
    'BackgroundColor', [1 1 1], ...
    'FontSize', 10);
end

function fieldUsage = inspectFieldUsage(pauseRuns)
fieldUsage = struct();
n = numel(pauseRuns);
fieldUsage.n_runs = n;
fieldUsage.has_DeltaM_signed = countFieldNonEmpty(pauseRuns, 'DeltaM_signed');
fieldUsage.has_FM_signed = countFieldFiniteScalar(pauseRuns, 'FM_signed');
fieldUsage.has_dip_signed = countFieldNonEmpty(pauseRuns, 'dip_signed');
fieldUsage.has_DeltaM = countFieldNonEmpty(pauseRuns, 'DeltaM');
fieldUsage.has_FM_step_raw = countFieldFiniteScalar(pauseRuns, 'FM_step_raw');
fieldUsage.has_DeltaM_sharp = countFieldNonEmpty(pauseRuns, 'DeltaM_sharp');

if fieldUsage.has_DeltaM_signed > 0
    fieldUsage.deltam_source = 'DeltaM_signed (canonical when available)';
else
    fieldUsage.deltam_source = 'fallback: DeltaM (legacy observable)';
end

if fieldUsage.has_FM_signed > 0
    fieldUsage.fm_source = 'FM_signed';
elseif fieldUsage.has_FM_step_raw > 0
    fieldUsage.fm_source = 'fallback: FM_step_raw';
else
    fieldUsage.fm_source = 'fallback: FM_step_mag or missing';
end

if fieldUsage.has_dip_signed > 0
    fieldUsage.dip_source = 'dip_signed';
elseif fieldUsage.has_DeltaM_sharp > 0
    fieldUsage.dip_source = 'fallback: DeltaM_sharp';
else
    fieldUsage.dip_source = 'fallback: unavailable';
end
end

