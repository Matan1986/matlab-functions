function [state, cfg, dataLog] = loadOrBuildState(repoRoot)
cfg = struct();
dataLog = struct();
dataLog.mode = '';
dataLog.notes = {};

try
    cfg = agingConfig();
catch ME
    dataLog.notes{end+1} = sprintf('agingConfig failed: %s', ME.message);
end

cfg = ensureCfgDefaults(cfg);

% Try real pipeline data first (same structs as pipeline stages).
try
    cfg = stage0_setupPaths(cfg);
    state = stage1_loadData(cfg);
    state = stage2_preprocess(state, cfg);
    state = stage3_computeDeltaM(state, cfg);
    state = stage4_analyzeAFM_FM(state, cfg);
    dataLog.mode = 'pipeline_stages_1_to_4';
    dataLog.notes{end+1} = sprintf('dataDir=%s', cfg.dataDir);
    return;
catch ME
    dataLog.notes{end+1} = sprintf('Pipeline stage build failed: %s', ME.message);
end

% Try real pipeline data by discovering dataDir from existing debug metrics.
try
    [cfg2, sourceMat] = cfgFromDebugMetricsSource(repoRoot, cfg);
    state = stage1_loadData(cfg2);
    state = stage2_preprocess(state, cfg2);
    state = stage3_computeDeltaM(state, cfg2);
    state = stage4_analyzeAFM_FM(state, cfg2);
    cfg = cfg2;
    dataLog.mode = 'pipeline_stages_1_to_4_from_debug_sourceFile';
    dataLog.notes{end+1} = sprintf('dataDir=%s', cfg.dataDir);
    dataLog.notes{end+1} = sprintf('debugMetricsMat=%s', sourceMat);
    return;
catch ME
    dataLog.notes{end+1} = sprintf('Debug-metrics derived build failed: %s', ME.message);
end

% Fallback: load latest saved state-like MAT artifact.
[state, matPath] = loadLatestStateSnapshot(repoRoot);
cfg = ensurePlotConfig(cfg, state);
if ~isfield(cfg, 'dataDir') || isempty(cfg.dataDir)
    cfg.dataDir = fileparts(matPath);
end
dataLog.mode = 'loaded_mat_snapshot';
dataLog.notes{end+1} = sprintf('loaded=%s', matPath);
end

function [state, matPath] = loadLatestStateSnapshot(repoRoot)
roots = {
    fullfile(repoRoot, 'results')
    fullfile(repoRoot, 'runs')
    fullfile(repoRoot, 'results_old')
    fullfile(repoRoot, 'Aging', 'results')
    };

allFiles = [];
for i = 1:numel(roots)
    if exist(roots{i}, 'dir')
        d = dir(fullfile(roots{i}, '**', '*.mat'));
        if ~isempty(d)
            allFiles = [allFiles; d(:)]; %#ok<AGROW>
        end
    end
end

if isempty(allFiles)
    error('No MAT files found for fallback state loading.');
end

[~, idxSort] = sort([allFiles.datenum], 'descend');
allFiles = allFiles(idxSort);

for i = 1:numel(allFiles)
    matPath = fullfile(allFiles(i).folder, allFiles(i).name);
    try
        vars = whos('-file', matPath);
        names = {vars.name};
        if any(strcmp(names, 'state'))
            S = load(matPath, 'state');
            if isStateLike(S.state)
                state = S.state;
                return;
            end
        end

        hasPauseRuns = any(strcmp(names, 'pauseRuns'));
        hasNoPauseT = any(strcmp(names, 'noPause_T'));
        hasNoPauseM = any(strcmp(names, 'noPause_M'));
        if hasPauseRuns && hasNoPauseT && hasNoPauseM
            S = load(matPath, 'pauseRuns', 'noPause_T', 'noPause_M');
            state = struct();
            state.pauseRuns = S.pauseRuns;
            state.noPause_T = S.noPause_T;
            state.noPause_M = S.noPause_M;
            return;
        end
    catch
        % Continue scanning.
    end
end

error('No state-like MAT snapshot was found in fallback directories.');
end

function [cfgOut, sourceMat] = cfgFromDebugMetricsSource(repoRoot, cfgIn)
cfgOut = ensureCfgDefaults(cfgIn);
sourceMat = '';

roots = {
    fullfile(repoRoot, 'results', 'aging')
    fullfile(repoRoot, 'results_old', 'aging')
    };

matFiles = [];
for i = 1:numel(roots)
    if exist(roots{i}, 'dir')
        d = dir(fullfile(roots{i}, '**', 'debug_metrics.mat'));
        if ~isempty(d)
            matFiles = [matFiles; d(:)];
        end
    end
end

if isempty(matFiles)
    error('No debug_metrics.mat files were found for dataDir discovery.');
end

[~, ord] = sort([matFiles.datenum], 'descend');
matFiles = matFiles(ord);

for i = 1:numel(matFiles)
    sourceMat = fullfile(matFiles(i).folder, matFiles(i).name);
    try
        S = load(sourceMat, 'debugTable');
        if ~isfield(S, 'debugTable') || isempty(S.debugTable)
            continue;
        end
        if ~ismember('sourceFile', S.debugTable.Properties.VariableNames)
            continue;
        end
        sourceFile = string(S.debugTable.sourceFile(1));
        if strlength(sourceFile) == 0
            continue;
        end
        sourceFile = char(sourceFile);
        dataDir = fileparts(sourceFile);
        if ~exist(dataDir, 'dir')
            continue;
        end
        cfgOut.dataDir = dataDir;
        cfgOut = stage0_setupPaths(cfgOut);
        return;
    catch
        % Continue scanning.
    end
end

error('Could not derive a valid dataDir from any debug_metrics.mat sourceFile.');
end

