function n = countFieldNonEmpty(s, fieldName)
n = 0;
for i = 1:numel(s)
    if isfield(s(i), fieldName) && ~isempty(s(i).(fieldName))
        n = n + 1;
    end
end
end

function n = countFieldFiniteScalar(s, fieldName)
n = 0;
for i = 1:numel(s)
    if isfield(s(i), fieldName) && ~isempty(s(i).(fieldName))
        v = s(i).(fieldName);
        if isnumeric(v) && isscalar(v) && isfinite(v)
            n = n + 1;
        end
    end
end
end

function idx = findLineContains(filePath, token)
idx = NaN;
if ~exist(filePath, 'file')
    return;
end
c = readlines(filePath);
for i = 1:numel(c)
    if contains(c(i), token)
        idx = i;
        return;
    end
end
end

function axOut = getOrderedAxes(hFig)
ax = findall(hFig, 'Type', 'axes');
if isempty(ax)
    axOut = gobjects(0);
    return;
end

keep = false(size(ax));
for i = 1:numel(ax)
    keep(i) = ~isempty(allchild(ax(i)));
end
if any(keep)
    ax = ax(keep);
end

pos = zeros(numel(ax), 4);
for i = 1:numel(ax)
