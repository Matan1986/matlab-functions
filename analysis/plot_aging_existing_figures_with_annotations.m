tl = tiledlayout(numel(allAxes), 1, 'TileSpacing', 'compact', 'Padding', 'compact'); %#ok<NASGU>

for i = 1:numel(allAxes)
    axDst = nexttile;
    cloneAxesContent(allAxes(i), axDst);
