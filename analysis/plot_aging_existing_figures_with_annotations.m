function cloneAxesContent(srcAx, dstAx)
copyobj(allchild(srcAx), dstAx);
set(dstAx, 'XLim', get(srcAx, 'XLim'));
set(dstAx, 'YLim', get(srcAx, 'YLim'));
set(dstAx, 'XScale', get(srcAx, 'XScale'));
set(dstAx, 'YScale', get(srcAx, 'YScale'));
grid(dstAx, get(srcAx, 'XGrid'));
box(dstAx, get(srcAx, 'Box'));

xlabel(dstAx, getLabelText(get(srcAx, 'XLabel')), 'Interpreter', 'none');
ylabel(dstAx, getLabelText(get(srcAx, 'YLabel')), 'Interpreter', 'none');
title(dstAx, getTitleText(srcAx), 'Interpreter', 'none');
end

function txt = getLabelText(hLabel)
txt = '';
if isempty(hLabel) || ~isgraphics(hLabel), return; end
v = get(hLabel, 'String');
txt = toText(v);
end

function txt = getTitleText(hAx)
txt = '';
if isempty(hAx) || ~isgraphics(hAx), return; end
hTitle = get(hAx, 'Title');
if isempty(hTitle) || ~isgraphics(hTitle), return; end
txt = toText(get(hTitle, 'String'));
end

function txt = toText(v)
if isstring(v)
    txt = char(join(v, ' '));
elseif iscell(v)
    try
        txt = strjoin(cellfun(@char, v, 'UniformOutput', false), ' ');
    catch
        txt = '';
    end
elseif ischar(v)
    txt = v;
else
    txt = '';
end
end

function out = toRel(pathIn)
if isempty(pathIn)
    out = '';
    return;
end
root = fileparts(fileparts(mfilename('fullpath')));
p = char(pathIn);
out = strrep(p, [root filesep], '');
end
