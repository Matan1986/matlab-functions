function tf = hasDecompositionFields(pauseRuns)
if isempty(pauseRuns)
    tf = false;
    return;
end
req = {'DeltaM_smooth', 'DeltaM_sharp'};
tf = true;
for i = 1:numel(req)
    if ~isfield(pauseRuns, req{i})
        tf = false;
        return;
    end
end
end

function Tp = pickExamplePauseK(pauseRuns)
Tp = NaN;
if isempty(pauseRuns) || ~isfield(pauseRuns, 'waitK')
    return;
end
vals = [pauseRuns.waitK];
vals = vals(isfinite(vals));
if isempty(vals)
    return;
end
Tp = vals(1);
end

function hFig = pickDeltaFigure(figCandidates)
hFig = [];
if isempty(figCandidates), return; end
for i = 1:numel(figCandidates)
    ax = findall(figCandidates(i), 'Type', 'axes');
    for j = 1:numel(ax)
        ylbl = getLabelText(get(ax(j), 'YLabel'));
        ttl = getTitleText(ax(j));
        if contains(lower(ylbl), 'delta') || contains(lower(ttl), 'delta') || ...
                contains(ylbl, '\Delta') || contains(ttl, '\Delta')
            hFig = figCandidates(i);
            return;
        end
    end
end
% Fallback: last created figure in this batch.
hFig = figCandidates(1);
end

function hFig = pickFigureByName(figCandidates, token)
hFig = [];
if isempty(figCandidates), return; end
for i = 1:numel(figCandidates)
    nm = string(get(figCandidates(i), 'Name'));
    if contains(lower(nm), lower(token))
        hFig = figCandidates(i);
        return;
    end
end
end

function [hCombined, combineInfo] = combineFiguresFromExisting(deltaFig, decompFig)
deltaAxes = getOrderedAxes(deltaFig);
decompAxes = getOrderedAxes(decompFig);
allAxes = [deltaAxes(:); decompAxes(:)];

if isempty(allAxes)
    error('No axes were found in source figures to combine.');
end

