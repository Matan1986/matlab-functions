figOutPng = fullfile(figDir, 'aging_existing_pipeline_decomposition.png');
reportOut = fullfile(repDir, 'aging_deltam_decomposition_existing_plot.md');

plotSources = discoverPlotSources(repoRoot);
[state, cfg, dataLog] = loadOrBuildState(repoRoot);
cfg = ensurePlotConfig(cfg, state);

if ~hasDecompositionFields(state.pauseRuns)
    state = stage4_analyzeAFM_FM(state, cfg);
end

% ---------------- Existing pipeline plot #1: DeltaM overview ----------------
figBefore = findall(groot, 'Type', 'figure');
plotAgingMemory(state.noPause_T, state.noPause_M, state.pauseRuns, cfg.color_scheme, ...
    cfg.fontsize, cfg.linewidth, cfg.sample_name, cfg.Bohar_units, ...
    cfg.offsetMode, cfg.offsetValue, cfg.dip_window_K, cfg.colorRange, cfg.useAutoYScale);
deltaFigCandidates = setdiff(findall(groot, 'Type', 'figure'), figBefore);
deltaFig = pickDeltaFigure(deltaFigCandidates);

% ---------------- Existing pipeline plot #2: AFM/FM decomposition ----------------
